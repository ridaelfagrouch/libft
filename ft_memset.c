#include "libft.h"

void	*ft_memset(void *buf, int c, size_t n)
{
	size_t	i;
	char	*str;

	i = 0;
	str = (char *) buf;
	while (i < n)
	{
		str[i] = c;
		i++;
	}
	return (0);
}
